        # –ü–æ–ª—É—á–∞–µ–º –≤—Å–µ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–µ –ø—Ä–µ–¥–º–µ—Ç—ã –¥–ª—è –±—ã—Å—Ç—Ä–æ–π –ø—Ä–æ–≤–µ—Ä–∫–∏
        existing_subjects = {s.code: s for s in Subject.query.all()}
        
        subjects_to_add = []
        
        for name, code in SUBJECTS.items():
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ —É–∂–µ –ø—Ä–µ–¥–º–µ—Ç —Å —Ç–∞–∫–∏–º –∫–æ–¥–æ–º
            if code in existing_subjects:
                existing = existing_subjects[code]
                # –û–±–Ω–æ–≤–ª—è–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ, –µ—Å–ª–∏ –æ–Ω–æ –∏–∑–º–µ–Ω–∏–ª–æ—Å—å
                if existing.name != name:
                    print(f"üîÑ –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ: {code} - '{name}'")
                    existing.name = name
                    existing.short_name = code
                    updated_count += 1
                else:
                    #print(f"‚è≠Ô∏è  –ü—Ä–æ–ø—É—Å–∫: {code} - —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç")
                    skipped_count += 1
